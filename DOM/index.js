// Select element by tag
console.log(document.getElementsByTagName('div'));

// Select element by ID
console.log(document.getElementById('boldTitle'));

// Select element by class
console.log(document.getElementsByClassName('mainImage'));

// Child nodes
console.log(document.body.childNodes)

// Query selector
console.log(document.querySelector('div')); // para tags

console.log(document.querySelector('#boldTitle')); // para IDs usar #

console.log(document.querySelector('.mainImage')); // para clases usar .
